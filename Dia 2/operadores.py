x=6
y=2
z=7

print(f"{x} mas {y} es igual a {x+y}")#suma
print(f"{x} menos {y} es igual a {x-y}")#resta
print(f"{x} por {y} es igual a {x*y}")#multiplicacion
print(f"{x} dividido {y} es igual a {x/y}")#division

print(f"{z} dividido {y} es igual a {z//y}")#division al piso#al utilizar el // le decimos a python que no queremos decimales con la respuesta solo el resultado de la division al piso
print(f"{z} dividido {y} es igual a {z%y}")#modulo
print(f"{z} dividido {y} es igual a {z**y}")#potenciacion
print(f"La raiz cuadrada de {x} es {x**0.5}")#raiz cuadrada #esta es una opercion ya que la potenciacino de 0.5 devuelve la raiz cuadrada de cualquier numero